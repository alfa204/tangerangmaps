package com.tangerangmaps.object;

public class ButtonItem {

	int text;
	int btnDrawable;
	
	
	public ButtonItem(int text, int btnDrawable) {
		super();
		this.text = text;
		this.btnDrawable = btnDrawable;
	}
	
	public int getText() {
		return text;
	}
	public int getBtnDrawable() {
		return btnDrawable;
	}
}
